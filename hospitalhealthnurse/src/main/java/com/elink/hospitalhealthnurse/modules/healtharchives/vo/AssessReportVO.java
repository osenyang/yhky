package com.elink.hospitalhealthnurse.modules.healtharchives.vo;

import java.io.Serializable;

/**
 * 健康建议Entity
 * @author 杜康
 * @version 2017-04-12
 */
public class AssessReportVO implements Serializable {
	
	private static final long serialVersionUID = 1L;

	private String id;	//编号
	private String archivesId; //档案ID    
	private String doctorNm; //医生姓名    
	private String content; //健康建议    
	private java.util.Date createDate; //创建日期    
	private String delFlag; //删除标记    

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	public String getArchivesId(){
		return archivesId;
	}   
		
	public void setArchivesId(String  archivesId){
		this.archivesId = archivesId;
	}   
	
	public String getDoctorNm() {
		return doctorNm;
	}

	public void setDoctorNm(String doctorNm) {
		this.doctorNm = doctorNm;
	}

	public String getContent(){
		return content;
	}   
		
	public void setContent(String  content){
		this.content = content;
	}   
	
	public java.util.Date getCreateDate(){
		return createDate;
	}   
		
	public void setCreateDate(java.util.Date  createDate){
		this.createDate = createDate;
	}   
	
	public String getDelFlag(){
		return delFlag;
	}   
		
	public void setDelFlag(String  delFlag){
		this.delFlag = delFlag;
	}   
	
}


